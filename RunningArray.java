public class RunningArray{

	public int totalIntElement(int[] list){
	int total = 0;
	
	for(int i = 0; i < list.length; i++){
	total += list[i];

        }

		return total;
     }
	public double totalDoubleElement(double[] list){
	double total = 0;
	
	for(int i = 0; i < list.length; i++){
	total += list[i];

        }

		return total;
      }

}