week = int(input(" Enter day of the week: "))
if week == 1:
	print("Monday")
if week == 2:
	print("Tuesday")
if week == 3:
	print("Wednesday")
if week == 4:
	print("Thursday")
if week == 5:
	print("Friday")
if week == 6:
	print("Saturday")
if week == 7:
	print("Sunday")
else:
	print("INVALID")

